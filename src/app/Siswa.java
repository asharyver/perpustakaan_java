    private void showData() {
        List<SiswaEntity> kat = new ArrayList<>();
        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `siswa`")) {
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                SiswaEntity siswa = new SiswaEntity();
                siswa.setKode(rs.getString("kd_siswa"));
                siswa.setNama(rs.getString("nm_siswa"));
                siswa.setNisn(rs.getString("nisn"));
                siswa.setKelamin(rs.getString("kelamin"));
                siswa.setAgama(rs.getString("agama"));
                siswa.setTempatLahir(rs.getString("tempat_lahir"));
                siswa.setTanggalLahir(rs.getString("tanggal_lahir"));
                siswa.setAlamat(rs.getString("alamat"));
                siswa.setTelp(rs.getString("no_telepon"));
                kat.add(siswa);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        SiswaTableModel tabelSiswa = new SiswaTableModel(kat);
        siswaTbl.setModel(tabelSiswa);
    }
    
