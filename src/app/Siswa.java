    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        String kd_siswa = kodeSiswaTxt.getText();
        try (Statement stmt = db.createStatement()) {
            stmt.execute("DELETE FROM `siswa` WHERE `kd_siswa` = '"+kd_siswa+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        siswaTbl.clearSelection();
    }//GEN-LAST:event_hapusBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        String kd_siswa = kodeSiswaTxt.getText();
        String nm_siswa = namaSiswaTxt.getText();
        String nisn = nisnSiswaTxt.getText();
        String jk = jkSiswaTxt.getSelection().getActionCommand();
        String agama = agamaSiswaTxt.getSelectedItem().toString();
        String tempat = telaSiswaTxt.getText();
        String tanggal = new java.sql.Date(talaSiswaTxt.getDate().getTime()).toString();
        String alamat = alamatSiswaTxt.getText();
        String telp = notelpSiswaTxt.getText();
        
        try (Statement stmt = db.createStatement()) {
            stmt.execute("UPDATE `siswa` SET "
                    + "`nm_siswa` = '"+nm_siswa+"', "
                    + "`nisn` = '"+nisn+"', "
                    + "`kelamin` = '"+jk+"', "
                    + "`agama` = '"+agama+"', "
                    + "`tempat_lahir` = '"+tempat+"', "
                    + "`tanggal_lahir` = '"+tanggal+"', "
                    + "`alamat` = '"+alamat+"', "
                    + "`no_telepon` = '"+telp+"' "
                + "WHERE `kd_siswa` = '"+kd_siswa+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        siswaTbl.clearSelection();
    }//GEN-LAST:event_editBtnActionPerformed

    private void simpanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBtnActionPerformed
        try (PreparedStatement ps = db.prepareStatement("INSERT INTO `siswa` VALUES (?,?,?,?,?,?,?,?,?)")) {
            java.sql.Date tanggal = new java.sql.Date(talaSiswaTxt.getDate().getTime());
            ps.setString(1, kodeSiswaTxt.getText());
            ps.setString(2, namaSiswaTxt.getText());
            ps.setString(3, nisnSiswaTxt.getText());
            ps.setString(4, jkSiswaTxt.getSelection().getActionCommand());
            ps.setString(5, agamaSiswaTxt.getSelectedItem().toString());
            ps.setString(6, telaSiswaTxt.getText());
            ps.setString(7, tanggal.toString());
            ps.setString(8, alamatSiswaTxt.getText());
            ps.setString(9, notelpSiswaTxt.getText());
            
            ps.execute();
        } catch (SQLException ex) {
            Logger.getLogger(Siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
    }//GEN-LAST:event_simpanBtnActionPerformed

    private void siswaTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_siswaTblMouseClicked
        TableModel table = siswaTbl.getModel();
        int row = siswaTbl.getSelectedRow();
        String id = table.getValueAt(row, 0).toString();

        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `siswa` WHERE `kd_siswa` = ?")) {
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                kodeSiswaTxt.setText(rs.getString("kd_siswa"));
                namaSiswaTxt.setText(rs.getString("nm_siswa"));
                nisnSiswaTxt.setText(rs.getString("nisn"));
                if (rs.getString("kelamin").equals("L")) {
                    jkLakiRdo.setSelected(true);
                } else {
                    jkPerRdo.setSelected(true);
                }
                agamaSiswaTxt.setSelectedItem(rs.getString("agama"));
                telaSiswaTxt.setText(rs.getString("tempat_lahir"));
                talaSiswaTxt.setDate(java.sql.Date.valueOf(rs.getString("tanggal_lahir")));
                alamatSiswaTxt.setText(rs.getString("alamat"));
                notelpSiswaTxt.setText(rs.getString("no_telepon"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Siswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        changeInputState(true);
        changeBtnState(true, "edit/delete");
        kodeSiswaTxt.setEnabled(false);
    }//GEN-LAST:event_siswaTblMouseClicked

