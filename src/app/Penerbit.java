    private void showData() {
        List<PenerbitEntity> kat = new ArrayList<>();
        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `penerbit`")) {
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                PenerbitEntity penerbit = new PenerbitEntity();
                penerbit.setKode(rs.getString("kd_penerbit"));
                penerbit.setNama(rs.getString("nm_penerbit"));
                kat.add(penerbit);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Penerbit.class.getName()).log(Level.SEVERE, null, ex);
        }
        PenerbitTableModel tabelPenerbit = new PenerbitTableModel(kat);
        penerbitTbl.setModel(tabelPenerbit);
    }
    
    private void reset() {
        kodePenerbitTxt.setText(null);
        namaPenerbitTxt.setText(null);
        penerbitTbl.clearSelection();
    }

    private void changeInputState(boolean b) {
        kodePenerbitTxt.setEnabled(b);
        namaPenerbitTxt.setEnabled(b);
    }
    
    private void changeBtnState(boolean b, String btn) {
        switch (btn) {
            case "new":
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(!b);
                editBtn.setEnabled(!b);
                simpanBtn.setEnabled(b);
            break;
            case "edit/delete":
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(b);
                editBtn.setEnabled(b);
                simpanBtn.setEnabled(!b);
            break;
            case "simpan": 
            default:
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(b);
                editBtn.setEnabled(b);
                simpanBtn.setEnabled(b);
            break;
        }
    }

