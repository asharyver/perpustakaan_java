    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        String kd_penerbit = kodePenerbitTxt.getText();
        try (Statement stmt = db.createStatement()) {
            stmt.execute("DELETE FROM `penerbit` WHERE `kd_penerbit` = '"+kd_penerbit+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Penerbit.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        penerbitTbl.clearSelection();
    }//GEN-LAST:event_hapusBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        String kd_penerbit = kodePenerbitTxt.getText();
        String nm_penerbit = namaPenerbitTxt.getText();
        try (Statement stmt = db.createStatement()) {
            stmt.execute("UPDATE `penerbit` SET `nm_penerbit` = '"+nm_penerbit+"' WHERE `kd_penerbit` = '"+kd_penerbit+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Penerbit.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        penerbitTbl.clearSelection();
    }//GEN-LAST:event_editBtnActionPerformed

    private void simpanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBtnActionPerformed
        try (PreparedStatement ps = db.prepareStatement("INSERT INTO `penerbit` VALUES (?,?)")) {
            ps.setString(1, kodePenerbitTxt.getText());
            ps.setString(2, namaPenerbitTxt.getText());
            ps.execute();
        } catch (SQLException ex) {
            Logger.getLogger(Penerbit.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
    }//GEN-LAST:event_simpanBtnActionPerformed

    private void penerbitTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_penerbitTblMouseClicked
        TableModel table = penerbitTbl.getModel();
        int row = penerbitTbl.getSelectedRow();
        String id = table.getValueAt(row, 0).toString();

        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `penerbit` WHERE `kd_penerbit` = ?")) {
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                kodePenerbitTxt.setText(rs.getString("kd_penerbit"));
                namaPenerbitTxt.setText(rs.getString("nm_penerbit"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Penerbit.class.getName()).log(Level.SEVERE, null, ex);
        }
        changeInputState(true);
        changeBtnState(true, "edit/delete");
        kodePenerbitTxt.setEnabled(false);
    }//GEN-LAST:event_penerbitTblMouseClicked

