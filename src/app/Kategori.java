    private void showData() {
        List<KategoriEntity> kat = new ArrayList<>();
        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `kategori`")) {
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                KategoriEntity kategori = new KategoriEntity();
                kategori.setKode(rs.getString("kd_kategori"));
                kategori.setNama(rs.getString("nm_kategori"));
                kat.add(kategori);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
        KategoriTableModel tabelKategori = new KategoriTableModel(kat);
        kategoriTbl.setModel(tabelKategori);
    }

    private void reset() {
        kodeKategoriTxt.setText(null);
        namaKategoriTxt.setText(null);
        kategoriTbl.clearSelection();
    }

    private void changeInputState(boolean b) {
        kodeKategoriTxt.setEnabled(b);
        namaKategoriTxt.setEnabled(b);
    }
    
    private void changeBtnState(boolean b, String btn) {
        switch (btn) {
            case "new":
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(!b);
                editBtn.setEnabled(!b);
                simpanBtn.setEnabled(b);
            break;
            case "edit/delete":
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(b);
                editBtn.setEnabled(b);
                simpanBtn.setEnabled(!b);
            break;
            case "simpan": 
            default:
                newBtn.setEnabled(!b);
                batalBtn.setEnabled(b);
                
                hapusBtn.setEnabled(b);
                editBtn.setEnabled(b);
                simpanBtn.setEnabled(b);
            break;
        }
    }

