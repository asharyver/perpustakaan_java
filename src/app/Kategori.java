    private void kategoriTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kategoriTblMouseClicked
        TableModel table = kategoriTbl.getModel();
        int row = kategoriTbl.getSelectedRow();
        id = table.getValueAt(row, 0).toString();

        try (PreparedStatement ps = db.prepareStatement("SELECT * FROM `kategori` WHERE `kd_kategori` = ?")) {
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                kodeKategoriTxt.setText(rs.getString("kd_kategori"));
                namaKategoriTxt.setText(rs.getString("nm_kategori"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
        changeInputState(true);
        changeBtnState(true, "edit/delete");
        kodeKategoriTxt.setEnabled(false);
    }//GEN-LAST:event_kategoriTblMouseClicked

    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        String kd_kategori = kodeKategoriTxt.getText();
        try (Statement stmt = db.createStatement()) {
            stmt.execute("DELETE FROM `kategori` WHERE `kd_kategori` = '"+kd_kategori+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        kategoriTbl.clearSelection();
    }//GEN-LAST:event_hapusBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        String kd_kategori = kodeKategoriTxt.getText();
        String nm_kategori = namaKategoriTxt.getText();
        try (Statement stmt = db.createStatement()) {
            stmt.execute("UPDATE `kategori` SET `nm_kategori` = '"+nm_kategori+"' WHERE `kd_kategori` = '"+kd_kategori+"'");
        } catch (SQLException ex) {
            Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
        showData();
        reset();
        changeInputState(false);
        changeBtnState(false, "simpan");
        kategoriTbl.clearSelection();
    }//GEN-LAST:event_editBtnActionPerformed

